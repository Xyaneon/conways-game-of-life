namespace Xyaneon.Games.ConwaysGameOfLife.Core;

/// <summary>
/// Represents a relative direction along the grid.
/// </summary>
public enum Direction
{
    /// <summary>
    /// Indicates the direction is down.
    /// </summary>
    Down,
    /// <summary>
    /// Indicates the direction is down-left.
    /// </summary>
    DownLeft,
    /// <summary>
    /// Indicates the direction is down-right.
    /// </summary>
    DownRight,
    /// <summary>
    /// Indicates the direction is left.
    /// </summary>
    Left,
    /// <summary>
    /// Indicates the direction is right.
    /// </summary>
    Right,
    /// <summary>
    /// Indicates the direction is up.
    /// </summary>
    Up,
    /// <summary>
    /// Indicates the direction is up-left.
    /// </summary>
    UpLeft,
    /// <summary>
    /// Indicates the direction is up-right.
    /// </summary>
    UpRight
}